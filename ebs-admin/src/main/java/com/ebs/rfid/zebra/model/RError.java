package com.ebs.rfid.zebra.model;


public class RError {
    private String error_no;
    private String msg;

    // Getters and Setters
    public String getError_no() {
        return error_no;
    }

    public void setError_no(String error_no) {
        this.error_no = error_no;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
